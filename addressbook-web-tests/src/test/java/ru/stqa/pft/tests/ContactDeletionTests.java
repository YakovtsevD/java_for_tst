package ru.stqa.pft.tests;// Generated by Selenium IDE

import org.testng.Assert;
import org.testng.annotations.BeforeMethod;
import org.testng.annotations.Test;
import ru.stqa.pft.model.ContactData;

import java.util.List;

public class ContactDeletionTests extends TestBase {

  @BeforeMethod
  public void ensurePreConditions() {
    app.goTo().gotoHome();
    if (!app.getContactHelper().isThereAContact()) {
      app.getContactHelper().createContact(new ContactData("Ivana", "Ivanova", "iva@tut.by", "newgroup777"), true);
    }
  }

  @Test (enabled = false)
  public void testContactDeletion() {
    List<ContactData> before = app.getContactHelper().getContactList();
    app.getContactHelper().selectContact(1);
    app.getContactHelper().deleteSelectedContacts();
    //assertThat(driver.switchTo().alert().getText(), is("Delete 1 addresses?"));
    app.getContactHelper().closeAlert();
    app.goTo().gotoHome();

    List<ContactData> after = app.getContactHelper().getContactList();
    Assert.assertEquals(after.size(), before.size()-1); // проверка что записей стало меньше на 1

    // проверка что остались нужные записи
    before.remove(1); // удаляем из списка before ту же запись что и тестом
    Assert.assertEquals(before, after);
    app.getSessionHelper().logout(); // разлогин
  }

}
